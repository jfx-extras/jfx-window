package jfxwindow.enums

/**
 * title-bar bottom border shadow depths.
 *
 * [DEPTH0] is equivalent to disabled shadow |
 * [DEPTH1] is bottom shadow edge level 1 |
 * [DEPTH2] is bottom shadow edge level 2 |
 * [DEPTH3] is bottom shadow edge level 3 |
 * [DEPTH4] is bottom shadow edge level 4 |
 * [DEPTH5] is bottom shadow edge level 5.
 */
@Suppress("RedundantVisibilityModifier")
public enum class TitleShadowDepth {
    DEPTH0,
    DEPTH1,
    DEPTH2,
    DEPTH3,
    DEPTH4,
    DEPTH5
}
